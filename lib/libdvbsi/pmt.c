static int parse_pmt_header(struct pmt *p_pmt, uint8_t *buf)
{
	uint16_t pos = 0;

	p_pmt->header_length = 0;
	pos = p_pmt->header_length;
	p_pmt->table_id = buf[pos + 0];
	p_pmt->section_syntax = (buf[pos + 1] & 0x80) >> 7;
	p_pmt->reserved_1 = (buf[pos + 1] & 0x30) >> 4;
	p_pmt->section_length = ((buf[pos + 1] & 0x03) << 8) | buf[pos + 2];

	p_pmt->program_number =	(buf[pos + 3] << 8) | buf[pos + 4];

	p_pmt->reserved_2 = (buf[pos + 5] & 0xc0) >> 6;
	p_pmt->version_number = (buf[pos + 5] & 0x3e) >> 1;
	p_pmt->current_next = buf[pos + 5] & 0x01;

	p_pmt->section_number = buf[pos + 6];
	p_pmt->last_section = buf[pos + 7];
	p_pmt->reserved_3 = (buf[pos + 8] & 0xe0) >> 5;

	p_pmt->pcr_pid = ((buf[pos + 8] & 0x1f) << 8) | buf[pos + 9];

	p_pmt->reserved_4 = (buf[pos + 10] & 0xf0) >> 4;
	p_pmt->program_info_length = ((buf[pos + 10] & 0x0f) << 8) | buf[pos + 11];

	printf("%s: Table ID=[%d], Section Length=[%d], Program Number=[%d], "
		"Section Number=[%d], PCR PID=[%d], Program info length=[%d]\n",
		__FUNCTION__, p_pmt->table_id, p_pmt->section_length,p_pmt->program_number,
		p_pmt->section_number,p_pmt->pcr_pid, p_pmt->program_info_length);

	pos += 12;
	p_pmt->header_length = pos;

	return pos;
}

static uint16_t parse_streams(struct streams *p_streams, uint8_t *buf,
					uint16_t stream_count, uint16_t pos, int bytes)
{
	uint16_t i;
	int end;

	p_streams->stream_type = buf[pos + 0];
	p_streams->reserved_1 = (buf[pos + 1] & 0xe0) >> 5;
	p_streams->elementary_pid = ((buf[pos + 1] & 0x1f) << 8) | buf[pos + 2];
	p_streams->reserved_2 = (buf[pos + 3] & 0xf0) >> 4;
	p_streams->es_info_length = ((buf[pos + 3] & 0x0f) << 8) | buf[pos + 4];
	p_streams->streams_desc_count = 0;

	printf("->%s: Elements=[", __FUNCTION__);
	for (i = 0; i < (p_streams->es_info_length + 5); i++)
		printf(" %02x", buf[pos + i]);
	printf("]\n");

	printf("->%s: Stream=[%d], Stream Type=[%d], Elementary PID=[%d], ES info length=[%d]\n",
				__FUNCTION__, stream_count, p_streams->stream_type,
				p_streams->elementary_pid, p_streams->es_info_length);


	pos += 5;
	end = pos + p_streams->es_info_length;
	p_streams->p_descriptors = malloc(sizeof (struct descriptor) * MAX_DESCRIPTORS);
	while (pos < end) {
		printf("->%s: Parsing descriptor %d:\n", __FUNCTION__, p_streams->streams_desc_count + 1);
		pos = parse_descriptor(&p_streams->p_descriptors[p_streams->streams_desc_count], buf, pos);
		p_streams->streams_desc_count++;
	}

	return pos;
}

